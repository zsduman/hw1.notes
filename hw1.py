#Hard Level
password_list = []

for char in range(1, nr_letters + 1):
  password_list.append(random.choice(letters))

for char in range(1, nr_symbols + 1):
  password_list += random.choice(symbols)

for char in range(1, nr_numbers + 1):
  password_list += random.choice(numbers)

print(password_list)
random.shuffle(password_list)
print(password_list)

password = ""
for char in password_list:
  password += char

print(f"Your password is: {password}")

## letters, symbols, ve numbers listeleri, farklı veri türlerini içerir:

## letters bir karakter (string) listesidir. Bu nedenle her karakteri ayrı bir liste öğesi olarak eklemek için append kullanılır.

## symbols ve numbers ise tek karakterden oluşan string değerlerdir. Bu nedenle bu karakterleri listeye eklemek için += operatörünü kullanabilirsiniz. Python'da bir string, karakterlerin bir dizisi olduğu için += operatörünü kullanarak karakteri listenin sonuna ekleyebilirsiniz.

## Bu nedenle, letters listesini append ile genişletmek ve symbols ile numbers listelerini += operatörü ile genişletmek, karakter türlerine uygun bir şekilde veri eklemenizi sağlar.

